x = 5
y = 6

# Logical AND
print(x < 10 and y < 10) # True
print(x > 10 and y < 10) # False

# Logical OR
print(x < 10 or y < 10)
print(x > 10 or y < 10)

# Logical NOT
print(not x < 10)


# Logical IS // is reference to the same object
print(x is y)

# Logical IS NOT // is reference to the same object
print(x is not y)